#ifndef CONSOLE_COLORS_H_
#define CONSOLE_COLORS_H_

#define  ANSI_COLOR_RED     "\x1b[31m" /*!< ANSI for red display */
#define  ANSI_COLOR_GREEN   "\x1b[32m" /*!< ANSI for green display */
#define  ANSI_COLOR_YELLOW  "\x1b[33m" /*!< ANSI for yellow display */
#define  ANSI_COLOR_BLUE    "\x1b[34m" /*!< ANSI for blue display */
#define  ANSI_COLOR_MAGENTA "\x1b[35m" /*!< ANSI for magenta display */
#define  ANSI_COLOR_CYAN    "\x1b[36m" /*!< ANSI for cyan display */
#define  ANSI_COLOR_RESET   "\x1b[0m"  /*!< ANSI to reset the display */

# endif /* CONSOLE_COLORS_H_ */
